const kotlin06Theory = `## Шартҳо (if / else)

### Аввал аз ҳаёт фаҳмем

**Чароғаки роҳ** медонед? Агар **САБЗ** бошад — мегузаред. Агар **СУРХ** бошад — меистед. Компютер ҳам **ҳамин тавр қарор мегирад**: агар шарт дуруст бошад, як кор; агар не — кори дигар.

### Баъд аз ин — дар код чӣ менависем?

\`\`\`kotlin
fun main() {
    val пул = 120
    // агар пул бас бошад, як паём; вагарна паёми дигар
    if (пул >= 100) {
        println("Метавонӣ себ харӣ!")
    } else {
        println("Пул намерасад")
    }
}
\`\`\`

Агар чанд ҳолат бошад, **else if** мегузорем — мисли чароғи се ранг.

### 🫖 Мисол аз ҳаёт

Агар **барф борид** — пальто пуш; вагарна либоси сода. **if** ҳамин «агар-вагарна»-и оддӣ аст.

### ⚠️ Хатои маъмул

Шартро дар кавита нагузор: \`if ("пул >= 100")\` нодуруст аст. Бояд бе кавита: \`if (пул >= 100)\`.

### 🎯 Хулоса

- if/else = чароғи роҳ: дуруст — як роҳ, нодуруст — роҳи дигар.
- Рақамҳоро бе кавита муқоиса мекунем.
- else if барои чанд ҳолат кӯмак мекунад.
`;

