export function getAllKotlinLessons(): Lesson[] {
  return kotlinLessonModules.flatMap((m) => m.lessons);
}

export function getKotlinLessonById(id: string): Lesson | undefined {
  return getAllKotlinLessons().find((l) => l.id === id);
}

export function getKotlinTotalLessonCount(): number {
  return getAllKotlinLessons().length;
}

/** Занҷири кушодан: танҳо модулҳои кушода, ба тартиби рақами дарс. */
export function getKotlinProgressionLessonIds(): string[] {
  return kotlinLessonModules
    .filter((m) => !m.locked)
    .flatMap((m) => [...m.lessons].sort((a, b) => a.number - b.number))
    .map((l) => l.id);
}

export function getKotlinModuleForLessonId(lessonId: string): LessonModule | undefined {
  return kotlinLessonModules.find((m) => m.lessons.some((l) => l.id === lessonId));
}

export function isKotlinLessonAccessible(lesson: Lesson): boolean {
  const mod = getKotlinModuleForLessonId(lesson.id);
  return Boolean(mod && !mod.locked);
}

export function getNextKotlinLessonId(currentId: string): string | null {
  const ordered = [...getAllKotlinLessons()].sort((a, b) => a.number - b.number);
  const i = ordered.findIndex((l) => l.id === currentId);
  if (i === -1 || i >= ordered.length - 1) return null;
  return ordered[i + 1]!.id;
}
